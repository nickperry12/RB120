# Question 10

# What are the benefits of using Object Oriented Programming in Ruby? Think of
# as many as you can.

=begin
1. It gives us a way to protect data by compartmentalizing it and hiding it from
   the public interface. By doing this, data cannot be manipulated without obvious
   intention.
   
2. It reduces the amount of dependencies in our code. We can make changes to our
   code without worrying about causing a cascade of problems and bugs in other
   parts of the code. Our code becomes the interaction of many small parts as
   opposed to a giant blob of dependencies.

3. It allows programmers to think more abstractly about the code they are writing.

4. Objects are represented by nouns so they are easier to conceptualize.

5. It allows us to only expose functionality to the parts of code that need it,
   meaning namespace issues are much harder to come across.

6. It allows us to give functionality to different parts of an application
   without duplication (polymorphism via inheritance)

7. As the software becomes more complex, the complexity can be easier maintained

8. Building applications becomes faster as we can re-use prewritten code.