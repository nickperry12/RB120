# There are a number of variables listed below. What are the different types and
# how do you know which is which?

excited_dog = "excited dog"
@excited_dog = "excited dog"
@@excited_dog = "excited dog"

# There are 3 different types of variables here. 

# 1. `excited_dog` is a local variable. There are no `@`s prepended to the name.

# 2. `@excited_dog` is an instance variable. This has one `@` prepended to the
# variable name.

# 3. `@@excited_dog` is a class variable. It has 2 `@`s prepended to the name. 