# What is the relationship between classes and objects in Ruby?

=begin
Classes are containers that store the attributes and behaviors of its objects,
and objects are instantiated from the class, and store states that are specific
to that object, i.e individual objects will contain different informaton than
other objects, yet they are both instances of the same class. Objects are also
used to expose the data and behaviors within the class. Classes can be thought
of molds and objects are what we create from those molds. 
=end